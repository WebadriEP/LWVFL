const mongoose = require("mongoose")
const Schema = mongoose.Schema

const donorSchema = new Schema({
  firstName: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  phone: {
    type: String,
    required: true,
  },
  address: {
    type: String,
    required: true,
  },
  addressLine2: {
    type: String,
  },
  city: {
    type: String,
    required: true,
  },
  state: {
    type: String,
    required: true,
  },
  zip: {
    type: String,
    required: true,
  },
  donations: [{type: mongoose.Schema.Types.ObjectId, ref: 'Donation'}],
})
  

module.exports = mongoose.model("Donor", donorSchema)